
public class tipoVariable {
	public static void main(String[] args) {
		System.out.println("Hola mundo");
		
		//int sirve para decalarar variables enteros
		int edad = 28;
		
		System.out.println("La edad es" + edad);
		
		//double sirve para asignar valores con decimal, el tipo de dato
		//double tiene el doble de bits de espacio por lo que podemos 
		//colocar un entero en un double
		
		double salario = 34.56;
		System.out.println(salario/2);
		
		//Las operaciones entre int siempre nos dara un int ejemplo:
		
		int regalos = 15;
		int personas = 2;
		System.out.print(regalos / personas);
	}
}
